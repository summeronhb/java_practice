
public class RelationalOp {
	public static void main(String[] args) {
		//복합관계연산자는 true/false 즉 boolean을 출력한다.
		
		System.out.println("3 >= 2 :" + (3>=2));
		System.out.println("3 >= 2 :" + (3<=2));
		
		// == 연산은 두 변수의 자료형이 다르더라도 자동형 변환을 해준 후 동등비교를 해준다.
		System.out.println("7.0 = 7 :" + (7.0 == 7));
		System.out.println("7.0 != 7 :" + (7.0 == 7));
		
	}

}
